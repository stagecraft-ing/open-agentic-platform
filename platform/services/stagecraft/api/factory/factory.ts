type DeployRequest = {
  id: string;
  environment: string;
  git_ref: string;
  registry_image?: string;
  actorUserId: string;
  workspaceId: string;
};

type DeployResponse = {
  deployment_id: string;
  target: string;
  status: string;
};

export const triggerDeploy = api(
  { expose: true, auth: true, method: "POST", path: "/api/projects/:id/factory/deploy" },
  async (req: DeployRequest): Promise<DeployResponse> => {
    const pipeline = await getActivePipeline(req.id, req.workspaceId);

    if (pipeline.status !== "completed") {
      throw APIError.failedPrecondition(
        `pipeline status is "${pipeline.status}", must be "completed" to deploy`
      );
    }

    if (!req.environment || !req.git_ref) {
      throw APIError.invalidArgument("environment and git_ref are required");
    }

    let deploymentId: string;
    let deployStatus = "queued";

    // Forward to deployd-api-rs if credentials are configured
    try {
      const { createPreviewDeployment, isDeploydConfigured } = await import(
        "../deploy/deploydClient"
      );

      if (isDeploydConfigured()) {
        const result = await createPreviewDeployment({
          tenant_id: "default",
          app_id: pipeline.projectId,
          env_id: req.environment,
          release_sha: req.git_ref,
          artifact_ref: req.registry_image ?? `ghcr.io/unknown:${req.git_ref}`,
          lane: "LANE_A",
        });
        deploymentId = result.release_id;
        deployStatus = result.status;
      } else {
        // No OIDC credentials configured — local-dev mode
        deploymentId = crypto.randomUUID();
        deployStatus = "queued_local";
      }
    } catch {
      // Graceful degradation: return a local deployment ID
      deploymentId = crypto.randomUUID();
      deployStatus = "queued_local";
    }

    // Audit
    await appendAudit({
      pipelineId: pipeline.id,
      event: "deployment_triggered",
      actor: req.actorUserId,
      details: {
        deployment_id: deploymentId,
        environment: req.environment,
        git_ref: req.git_ref,
        registry_image: req.registry_image,
      },
    });

    await FactoryEventTopic.publish({
      project_id: req.id,
      pipeline_id: pipeline.id,
      event_type: "deployment_triggered",
      actor: req.actorUserId,
      details: { environment: req.environment, deployment_id: deploymentId },
    });

    return {
      deployment_id: deploymentId,
      target: req.environment,
      status: deployStatus,
    };
  }
);

