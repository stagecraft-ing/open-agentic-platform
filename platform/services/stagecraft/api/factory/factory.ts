type PipelineSource = "opc-direct" | "stagecraft";

type InitRequest = {
  id: string; // project ID (path param)
  adapter: string;
  business_docs?: BusinessDocRef[];
  knowledge_object_ids?: string[]; // spec 087: resolve workspace knowledge objects
  policy_overrides?: PolicyOverrides;
  /**
   * Trigger path for this pipeline (spec 110 §8 Rollout Phase 3).
   *
   *   - "opc-direct" (default): the caller runs the engine locally; no
   *     factory.run.request envelope is dispatched. Preserves the legacy
   *     flow for offline and OPC-originated runs.
   *   - "stagecraft": stagecraft dispatches a `factory.run.request` through
   *     the duplex channel so a connected OPC executes the run. Used by the
   *     web Initialize button and `oap-ctl run factory`.
   *
   * Defaults to "opc-direct" while the flag is off by default (spec 110 §8
   * Rollout Phase 3). Flipping the default is Rollout Phase 6.
   */
  source?: PipelineSource;
  actorUserId: string;
  workspaceId: string;
};

type InitResponse = {
  pipeline_id: string;
  adapter: string;
  policy_bundle_id: string;
  status: string;
  source: PipelineSource;
  created_at: string;
};

export const initPipeline = api(
  { expose: true, auth: true, method: "POST", path: "/api/projects/:id/factory/init" },
  async (req: InitRequest): Promise<InitResponse> => {
    await verifyProjectInWorkspace(req.id, req.workspaceId);

    // Validate adapter
    if (!KNOWN_ADAPTERS.includes(req.adapter as typeof KNOWN_ADAPTERS[number])) {
      throw APIError.invalidArgument(
        `unknown adapter "${req.adapter}". Known adapters: ${KNOWN_ADAPTERS.join(", ")}`
      );
    }

    // Resolve knowledge objects into business doc refs (spec 087 Phase 2)
    let knowledgeDocs: BusinessDocRef[] = [];
    if (req.knowledge_object_ids && req.knowledge_object_ids.length > 0) {
      knowledgeDocs = await resolveKnowledgeForFactory(
        req.workspaceId,
        req.knowledge_object_ids
      );
    }

    // Merge explicit business_docs with resolved knowledge objects
    const allDocs = [...(req.business_docs ?? []), ...knowledgeDocs];

    const source: PipelineSource = req.source ?? "opc-direct";

    // All inserts in a single transaction to prevent orphaned rows
    const result = await db.transaction(async (tx) => {
      // Compile policy bundle
      const rules = compileDefaultRules(req.adapter, req.policy_overrides);
      const [bundle] = await tx
        .insert(factoryPolicyBundles)
        .values({
          projectId: req.id,
          adapterName: req.adapter,
          rules,
        })
        .returning();

      // Find most recent completed pipeline for same project+adapter (082 FR-022)
      const prevRows = await tx
        .select({ id: factoryPipelines.id })
        .from(factoryPipelines)
        .where(
          and(
            eq(factoryPipelines.projectId, req.id),
            eq(factoryPipelines.adapterName, req.adapter),
            eq(factoryPipelines.status, "completed")
          )
        )
        .orderBy(desc(factoryPipelines.createdAt))
        .limit(1);

      const previousPipelineId = prevRows.length > 0 ? prevRows[0].id : null;

      // Create pipeline
      const [pipeline] = await tx
        .insert(factoryPipelines)
        .values({
          projectId: req.id,
          adapterName: req.adapter,
          policyBundleId: bundle.id,
          previousPipelineId,
          source,
        })
        .returning();

      // Store business document references (explicit + knowledge-resolved)
      if (allDocs.length > 0) {
        await tx.insert(factoryBusinessDocs).values(
          allDocs.map((doc) => ({
            pipelineId: pipeline.id,
            name: doc.name,
            storageRef: doc.storage_ref,
          }))
        );
      }

      // Seed stage rows
      await tx.insert(factoryStages).values(
        PIPELINE_STAGES.map((stageId) => ({
          pipelineId: pipeline.id,
          stageId,
        }))
      );

      // Audit entry
      await appendAudit(
        {
          pipelineId: pipeline.id,
          event: "pipeline_initialized",
          actor: req.actorUserId,
          details: {
            adapter: req.adapter,
            doc_count: allDocs.length,
            knowledge_object_ids: req.knowledge_object_ids ?? [],
            policy_bundle_id: bundle.id,
            source,
          },
        },
        tx as unknown as typeof db,
      );

      return { pipeline, bundle };
    });

    await FactoryEventTopic.publish({
      project_id: req.id,
      pipeline_id: result.pipeline.id,
      event_type: "pipeline_initialized",
      actor: req.actorUserId,
      details: { adapter: req.adapter, doc_count: allDocs.length, source },
    });

    // Spec 110 §8 Rollout Phase 3: dispatch the duplex `factory.run.request`
    // only when the trigger path was `stagecraft`. OPC-direct runs still
    // execute the legacy local path; their envelope dispatch is a no-op.
    if (source === "stagecraft") {
      await publishFactoryRunRequest({
        workspaceId: req.workspaceId,
        projectId: req.id,
        pipelineId: result.pipeline.id,
        adapter: req.adapter,
        actorUserId: req.actorUserId,
        knowledgeObjectIds: req.knowledge_object_ids ?? [],
        businessDocs: (req.business_docs ?? []).map((d) => ({
          name: d.name,
          storageRef: d.storage_ref,
        })),
        policyBundleId: result.bundle.id,
      });
    }

    return {
      pipeline_id: result.pipeline.id,
      adapter: result.pipeline.adapterName,
      policy_bundle_id: result.bundle.id,
      status: result.pipeline.status,
      source,
      created_at: result.pipeline.createdAt.toISOString(),
    };
  }
);

