async function verifyProjectInWorkspace(
  projectId: string,
  workspaceId: string
): Promise<void> {
  const rows = await db
    .select({ id: projects.id })
    .from(projects)
    .where(and(eq(projects.id, projectId), eq(projects.workspaceId, workspaceId)))
    .limit(1);

  if (rows.length === 0) {
    throw APIError.notFound("project not found");
  }
}

async function getActivePipeline(
  projectId: string,
  workspaceId: string
): Promise<PipelineRow> {
  // Workspace-scoped: verify project belongs to workspace
  await verifyProjectInWorkspace(projectId, workspaceId);

  const rows = await db
    .select()
    .from(factoryPipelines)
    .where(eq(factoryPipelines.projectId, projectId))
    .orderBy(desc(factoryPipelines.createdAt))
    .limit(1);

  if (rows.length === 0) {
    throw APIError.notFound("no Factory pipeline found for this project");
  }
  return rows[0];
}

async function appendAudit(
  entry: {
    pipelineId: string;
    event: string;
    actor?: string;
    stageId?: string;
    featureId?: string;
    details?: Record<string, unknown>;
  },
  txDb?: typeof db,
): Promise<string> {
  const d = txDb ?? db;
  const [row] = await d
    .insert(factoryAuditLog)
    .values({
      pipelineId: entry.pipelineId,
      event: entry.event,
      actor: entry.actor ?? null,
      stageId: entry.stageId ?? null,
      featureId: entry.featureId ?? null,
      details: entry.details ?? {},
    })
    .returning({ id: factoryAuditLog.id });
  return row.id;
}

